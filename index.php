    <h1>Kinetic Art Computing Engine</h1>
    <p>
      The goal of this project is to create a simulation to model kinetic art capable of computation.
      Balls rolling down ramps in combination with some moving parts provide the medium for computation.
    </p>
    
    <p>Demos</p>
    <ul>
      <li><a href="FallingBallDemo.php">Falling Ball Demo</a>: Lays out the basic frame work and shows a ball falling on a grid.</li>
      <li><a href="RampDemo.php">Ramp Demo</a>: Ball falls, hits a ramp, is deflected and continues falling</li>
      <li><a href="RampDemo2.php">Ramp Demo 2</a>: Just having some fun</li>
      <li><a href="VectorIntersectionTest.php">Vector Intersection Test</a>: Tests a set of vector representing motion intersecting a set of vectors representing an obstacle.</li>
      <li><a href="ToggleDemo.php">Toggle Demo</a>: Ball falls, hits a toggle, toggle changes state and deflects the ball</li>
      <li><a href="Adder.php">Adder</a>: A simulation of <a href="http://www.youtube.com/watch?v=GcDshWmhF4A" target="_blank">this</a> adder</li>
      <li><a href="TrapDoorDemo.php">Trap Door Demo</a>: A ball falls onto a trap door which can be opened an closed.</li>
      <li><a href="AndOrGateDemo.php">And Or Gate Demo</a>: This is one of the base logic units. A ramp with a gap. The first ball to roll down the ramp fills the gap, the second will roll over it.</li>
      <li><a href="TeleportDemo.php">Teleporter Demo</a>: Provides an In gate and an Out gate. This is meants to allow basic simulations of 3D in a 2D plane.</li>
      <li><a href="TeleportCrossDemo.php">Teleporter Demo 2</a>: A slightly more useful demo.</li>
      <li><a href="TeleportDemo3.php">Teleporter Demo 3</a>: A slightly less useful (but more fun) demo.</li>
      <li><a href="PistonDemo.php">Piston Demo</a>: The double piston serves as both NOT and COPY. In electronics, copy is trivial, but not in marbles.</li>
      <li><a href="trashDemo.php">Trash Demo</a>: Deletes a ball.</li>
      <li><a href="FullAdder.php">Full Adder</a>: Takes up to 3 balls and ouput a sum and a carry</li>
      <li><a href="MultiAdder.php">Multi Adder</a>: Multi stage binary adder with cascading carry</li>
    </ul>
    
